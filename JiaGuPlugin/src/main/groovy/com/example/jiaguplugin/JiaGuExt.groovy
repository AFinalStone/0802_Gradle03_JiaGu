package com.example.jiaguplugin;

public class JiaGuExt {
    private userName;
    private password;
    private keyStoreFile;
    private keyStorePassword;
    private keyAlias;
    private keyPassword;
    private jiaguToolPath;

    public getUserName() {
        return userName;
    }

    public void setUserName(userName) {
        this.userName = userName;
    }

    public getPassword() {
        return password;
    }

    public void setPassword(password) {
        this.password = password;
    }

    public getKeyStoreFile() {
        return keyStoreFile;
    }

    public void setKeyStoreFile(keyStoreFile) {
        this.keyStoreFile = keyStoreFile;
    }

    public getKeyStorePassword() {
        return keyStorePassword;
    }

    public void setKeyStorePassword(keyStorePassword) {
        this.keyStorePassword = keyStorePassword;
    }

    public getKeyAlias() {
        return keyAlias;
    }

    public void setKeyAlias(keyAlias) {
        this.keyAlias = keyAlias;
    }

    public getKeyPassword() {
        return keyPassword;
    }

    public void setKeyPassword(keyPassword) {
        this.keyPassword = keyPassword;
    }

    public getJiaguToolPath() {
        return jiaguToolPath;
    }

    public void setJiaguToolPath(jiaguToolPath) {
        this.jiaguToolPath = jiaguToolPath;
    }
}
